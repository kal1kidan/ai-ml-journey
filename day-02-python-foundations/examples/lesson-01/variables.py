# Lesson 1: Variables in Python

# Creating variables
name = "Kal"
age = 21
height = 1.61

# Printing variable values
print(name)
print(age)
print(height)

# Variables can be reassigned
age = age + 1

print("New age:", age)

# Checking variable types
print(type(name))
print(type(age))
print(type(height))